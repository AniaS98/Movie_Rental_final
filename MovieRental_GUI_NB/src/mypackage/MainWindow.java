    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        if(system.CheckLoginPassword(loginText.getText(), String.valueOf(passwordText.getPassword())) != null){
            Client client = new Client();
            String login = loginText.getText();
            String password = String.valueOf(passwordText.getPassword());
            client = system.CheckLoginPassword(login, password);
            //System.out.println("Login: " + loginText.getText() + " Has≈Ço: " + passwordText.getText());
            UserAccount frame = new UserAccount(system, client, movies);
            frame.pack();
            frame.setVisible(true);
        }
        else {
            JOptionPane.showMessageDialog(null, "Password or login is incorrect", "Attention!", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_loginButtonActionPerformed

